void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)// Get ссылается на место в массиве
    {
       for (int j = 0; j < image.GetLength(1); j++)
        {
            //Console.Write($"{image[i, j]} ");
            if(image[i,j] == 0) Console.Write($" "); // если будет== 0 тогда ПРОБЕЛ
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)  //указываем позиц строки и пиксиля для закраски
{
    if (pic [row, col] == 0) // если позиция не 1 то..
    {
        pic[row, col] =1;
        FillImage(row-1, col);  // РЕКУРСИЯ вызывает сам себя 
        FillImage(row, col-1); 
        FillImage(row+1, col); 
        FillImage(row, col+1); 
    }
}

